export function AdminSidebar() {
  const dispatch = useDispatch();
  const sidebarOpen = useSelector((s) => s.admin.sidebarOpen);
  const { isFullAdmin } = useAuth();

  const sections = NAV_SECTIONS.map((section) => ({
    ...section,
    items: section.title === 'System' && !isFullAdmin
      ? section.items.filter((item) => item.to !== '/admin/settings')
      : section.items,
  }));

  return (
    <>
      {sidebarOpen && (
        <div className="fixed inset-0 z-40 bg-black/50 lg:hidden" onClick={() => dispatch(setSidebarOpen(false))} aria-hidden="true" />
      )}
      <aside className={cn(
        'fixed inset-y-0 left-0 z-50 flex w-64 flex-col border-r border-border bg-card transition-transform duration-300 lg:static lg:translate-x-0',
        sidebarOpen ? 'translate-x-0' : '-translate-x-full',
      )}>
        <div className="flex h-14 items-center gap-2 border-b border-border px-4">
          <span className="flex h-8 w-8 items-center justify-center rounded-lg bg-destructive text-xs font-bold text-destructive-foreground">A</span>
          <div>
            <span className="font-semibold">Admin Panel</span>
            <p className="text-[10px] text-muted-foreground">Enterprise Console</p>
          </div>
        </div>
        <nav className="flex-1 overflow-y-auto p-3">
          {sections.map((section) => (
            <div key={section.title} className="mb-4">
              <p className="mb-1 px-3 text-[10px] font-semibold uppercase tracking-wider text-muted-foreground">{section.title}</p>
              <div className="space-y-0.5">
                {section.items.map((item) => (
                  <NavLink
                    key={item.to}
                    to={item.to}
                    end={item.end}
                    onClick={() => dispatch(setSidebarOpen(false))}
                    className={({ isActive }) => cn(
                      'flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors',
                      isActive ? 'bg-destructive/10 text-destructive' : 'text-muted-foreground hover:bg-accent hover:text-foreground',
                    )}
                  >
                    <NavIcon d={item.icon} />
                    {item.label}
                  </NavLink>
                ))}
              </div>
            </div>
          ))}
        </nav>
      </aside>
    </>
  );
}
